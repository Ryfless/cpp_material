#include <iostream>
using namespace std;

int main() {
  int a, b, hasil;
  a = 2;
  b = 3;
  hasil = a + b; /* <-- Operasi aritmatika dapat menggunakan +, -, /, *, % 
                        + untuk Menambah
                        - untuk Mengurangi
                        / untuk Membagi
                        * untuk Mengali
                        % untuk Modulo (sisa bagi)
                */
  cout << hasil << endl;
  
  return 0;
}

/* Output = 5 */
